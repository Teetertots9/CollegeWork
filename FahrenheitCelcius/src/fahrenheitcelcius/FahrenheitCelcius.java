public class FahrenheitCelcius {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        //Data
        //input data    
            int fahrenheit;
        //output data    
            double celcius;
        //input
            fahrenheit = 35;
        //process    
            celcius = (5/9)*(fahrenheit - 32);
        //output
            System.out.println("35 degrees fahrenheit is:" + celcius);
        
        
        
        
        
    }
    
}
